package Day09;

public class Student {
        private int no;
        private String name;
        private int age;
        private boolean sex;
        private String addr;

        public int getNo() {
                return no;
        }

        public void setNo(int no) {
                this.no = no;
        }

        public String getName() {
                return name;
        }

        public void setName(String name) {
                this.name = name;
        }

        public int getAge() {
                return age;
        }

        public void setAge(int age) {
                this.age = age;
        }

        public boolean isSex() {
                return sex;
        }

        public void setSex(boolean sex) {
                this.sex = sex;
        }

        public String getAddr() {
                return addr;
        }

        public void setAddr(String addr) {
                this.addr = addr;
        }
}
